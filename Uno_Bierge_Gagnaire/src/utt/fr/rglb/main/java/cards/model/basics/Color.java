package utt.fr.rglb.main.java.cards.model.basics;

/**
 * Enumération contenant les différentes valeurs de couleur de cartes
 * </br>Valeurs possibles :
 * </br> - {@code RED} (rouge)
 * </br> - {@code BLUE} (bleu)
 * </br> - {@code GREEN} (vert)
 * </br> - {@code YELLOW} (jaune)
 * </br> - {@code JOKER} (joker/sans-couleur)
 */
public enum Color {
	RED,		//Rouge
	BLUE,		//Bleu
	GREEN,		//Vert
	YELLOW,		//Jaune
	JOKER;		//Joker (sans couleur)
}
